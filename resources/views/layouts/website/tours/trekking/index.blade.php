	            	<p>The scope for trekking in Bhutan is almost boundless in variety and depth. Rocky mountains, captivating valleys, winding rivers, sparkling lakes, thick forests and the unexplored purity of nature all make Bhutan a trekkers’ paradise. From the towering snow peaks of the inner Himalayas to the verdant forests which cloak the hills above peaceful valleys, the trails are clean and unspoiled.  Treks can range from relatively light-effort day trips to expeditions lasting weeks. Trekking is an ideal way to experience rural Bhutanese culture and the exotic, unfamiliar wilderness. The countryside is endowed with a wide variety of flora and fauna, varying in setting from subtropical dense forest to alpine shrubs. The landscape is sparsely inhabited by scattered settlements enjoying diverse traditions. Despite the simple and relatively hard living conditions of local people, visitors will be impressed with their hospitality.  In general, trekking in Bhutan is physically demanding because of the frequent and abrupt changes in elevation which are typical of the terrain. Trekking is a year round activity, but most routes are suited to particular seasons. If you wish to see the alpine flowers you should visit us during the summer season , most likely during July and August. During the fall/autumn the nights are cold but the days are clear, rewarding the visitor with spectacular views and unforgettable photo opportunities. Late spring is warmer and is the flowering season for native varieties of the Himalayan rhododendron, refreshed by the greater likelihood of rain.</p>
